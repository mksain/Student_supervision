    public void searchFees()
    {
        int a1=Integer.parseInt(jTextField1.getText());
        try
        {
            String sql="Select * from registration where Registration_no="+a1;
            ps=conn.prepareStatement(sql);
            rs=ps.executeQuery();
            
            if(rs.next())
            {
                jTextField1.setText(rs.getString(1));
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(18));
                jTextField4.setText(rs.getString(3));
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Incorrect no.");
            }
            ps.close();
            rs.close();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    
