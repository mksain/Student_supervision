    public void search()
    {
        int a1=Integer.parseInt(jTextField1.getText());
        try
        {
            String sql="Select * from registration where Registration_no="+a1;
            ps=conn.prepareStatement(sql);
            rs=ps.executeQuery();
            
            if(rs.next())
            {
                jTextField1.setText(rs.getString(1));
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(18));
                jTextField4.setText(rs.getString(6));
                jTextField5.setText(rs.getString(7));
                jTextField6.setText(rs.getString(8));
                jComboBox1.setSelectedItem(rs.getString(20));
                jTextField7.setText(rs.getString(11));
                jTextField8.setText(rs.getString(15));
            }
            else
            {
                JOptionPane.showMessageDialog(null,"searching Done");
            }
            ps.close();
            rs.close();
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    public void submit()
    {
        try
        {
            String sql="insert into University values(?,?,?,?,?,?,?,?,?,?,?)";
            ps=conn.prepareStatement(sql);
            ps.setInt(1,Integer.parseInt(jTextField1.getText()));
            ps.setString(2,jTextField2.getText());
            ps.setString(3,jTextField3.getText());
            ps.setString(4,jTextField4.getText());
            ps.setString(5,jTextField5.getText());
            ps.setString(6,jTextField6.getText());
            ps.setString(7,(String)jComboBox1.getSelectedItem());
            ps.setString(8,jTextField7.getText());
            ps.setString(9,jTextField8.getText());
            ps.setString(10,jTextField9.getText());
            ps.setString(11,jTextField10.getText());
            ps.execute();
            JOptionPane.showMessageDialog(null,"Submit succesfull");
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
